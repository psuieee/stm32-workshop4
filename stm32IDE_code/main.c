/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{

  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_USART2_UART_Init();
  MX_I2C1_Init();
  /* USER CODE BEGIN 2 */

  /* ---- Initialize MPU-6050 using Nick's library ---- */
  HAL_StatusTypeDef mpu_status = mpu6050_init(&hi2c1,
                                               MPU_ACCEL_RANGE_2G,
                                               MPU_GYRO_RANGE_250DPS);

  if (mpu_status != HAL_OK)
  {
      /* Fast-blink LD2 to signal init failure */
      while (1)
      {
          HAL_GPIO_TogglePin(LD2_GPIO_Port, LD2_Pin);
          HAL_Delay(100);
      }
  }

  /* Set DLPF to ~44 Hz bandwidth (setting 3) — good anti-aliasing for 50 Hz */
  mpu6050_setDLPF(3);

  /* Solid LED = sensor ready, waiting for button press */
  HAL_GPIO_WritePin(LD2_GPIO_Port, LD2_Pin, GPIO_PIN_SET);

  /* Data variables */
  mpu6050_3DData accel;
  mpu6050_3DData gyro;
  uint32_t timestamp_ms = 0;
  char tx_buf[128];

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    /* PC13 reads LOW when the blue button is pressed (active-low).
     * Only stream data while the button is held down. */
    if (HAL_GPIO_ReadPin(B1_GPIO_Port, B1_Pin) == GPIO_PIN_RESET)
    {
        /* Read accelerometer (m/s^2) and gyroscope (deg/s) */
        HAL_StatusTypeDef s1 = mpu6050_getAccelData(&accel);
        HAL_StatusTypeDef s2 = mpu6050_getGyroData(&gyro);

        if (s1 == HAL_OK && s2 == HAL_OK)
        {
            int len = snprintf(tx_buf, sizeof(tx_buf),
                "%lu,%.4f,%.4f,%.4f,%.4f,%.4f,%.4f\r\n",
                timestamp_ms,
                accel.x, accel.y, accel.z,
                gyro.x,  gyro.y,  gyro.z);

            HAL_UART_Transmit(&huart2, (uint8_t *)tx_buf, len, HAL_MAX_DELAY);
        }

        /* LED on while recording */
        HAL_GPIO_WritePin(LD2_GPIO_Port, LD2_Pin, GPIO_PIN_SET);

        timestamp_ms += SAMPLE_PERIOD_MS;
    }
    else
    {
        /* Button released — reset timestamp so each gesture starts at 0 */
        timestamp_ms = 0;

        /* LED off when idle */
        HAL_GPIO_WritePin(LD2_GPIO_Port, LD2_Pin, GPIO_PIN_RESET);
    }

    HAL_Delay(SAMPLE_PERIOD_MS);

    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

